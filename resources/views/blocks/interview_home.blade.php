                            <b>
                                - С чего же начинает свою деятельность Федеральная Палата налоговых консультантов?
                            </b>
                    </p>
                    <p>
                        <div>
                            <p>- У нас большие планы, которые мы будем реализовывать последовательно. Для аттестованных налоговых консультантов мы предусмотрели особый порядок включения в федеральный реестр. Указанный порядок  не предусматривает дополнительной переэкзаменовки. Причина этому – особое уважительное отношение к статусу аттестованного налогового консультанта, вне зависимости от того, какое профессиональное сообщество подтвердило соответствие его квалификации. Каждый аттестованный налоговый консультант будет включен в федеральный реестр налоговых консультантов на основании заявления.  </p>
                            <p>Сегодня назрела необходимость создать отвечающую современным потребностям общества систему подготовки и аттестации консультантов по налогам и сборам.  Речь идет именно о внедрении системы, позволяющей использовать все возможности профессионального сообщества и повсеместно добиваться высокого качества подготовки, а не только ориентироваться на отдельные истории успешного обучения налоговых консультантов. Это огромная задача, но мы не новички в этом деле и для ее реализации имеем достаточный опыт и потенциал.</p>
                            <p>Всех будущих налоговых консультантов очень волнует тема сдачи квалификационного экзамена. Мы уже приступили к созданию тренажера для подготовки к экзамену. Это будет тот ресурс, который позволит каждому претенденту, независимо от места проживания и обучения, проводить самостоятельный мониторинг  полученных знаний и навыков, добиваться их совершенствования, четко видеть темы и вопросы, требующие дополнительной проработки. Процедура сдачи экзамена тоже будет основана на внедрении новых форматов и использовании  современных технологий.  </p>
                            <p>Федеральная Палата налоговых консультантов планирует активно развивать наставничество в отношении начинающих налоговых консультантов и оказывать поддержку опытным специалистам. Это позволит минимизировать профессиональные риски как новичков в налоговом консультировании, так и состоявшихся профессионалов. Для многих налоговых консультантов подобная практика и поддержка снимет множество барьеров и препятствий на всех этапах профессиональной деятельности.</p>
                            <p>Для образовательных организаций, осуществляющих обучение налоговых консультантов, ФПНК предлагает порядок взаимодействия, предусматривающий исключительно паритетные отношения. Акцент сделан в пользу свободы выбора объема программы, проявления творческой инициативы и профессионализма коллективами образовательных организаций, что позволит каждому вузу и учебному центру в буквальном смысле раскрыться.</p>
