                            - О статусе квалификационного аттестата мы много рассуждали и раньше, будем рассуждать и сейчас. Несмотря на то, что юридических преимуществ в налоговом консалтинге аттестат не дает, его роль и значимость велики. Наличие аттестата означает, что его обладатель освоил сложную образовательную программу, справился с интенсивной учебной нагрузкой, смог сдать экзамен и таким образом подтвердил свою квалификацию в среде профессионалов. Это очень важные и значимые характеристики специалиста. Соответственно, в бизнесе квалификационный аттестат может стать конкурентным преимуществом, выгодно отличающим его обладателя от других консультантов, а если речь идет о работе по найму, то аттестат будет дополнительным активом соискателя при трудоустройстве. 
