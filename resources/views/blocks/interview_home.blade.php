                            <b>
                                - Программы подготовки налоговых консультантов ФПНК будут отличаться от тех, по которым сейчас проводится обучение? 
                            </b>
                    </p>
                    <p>
                        <div>
                            - По моему мнению, программы подготовки налоговых консультантов, рекомендованные профессиональными сообществами или разработанные образовательными организациями самостоятельно, не могут иметь существенных отличий, поскольку все они нацелены на достижение единого результата – подготовку грамотных, компетентных налоговых консультантов. Таким образом, планируемые результаты обучения очень схожи. Более того, налоговые консультанты, аттестованные в разных профессиональных сообществах, должны справляться с идентичными профессиональными задачами. Это и определяет набор дисциплин в учебных планах, а также перечень компетенций, которые должны быть сформированы у обучающихся в результате освоения программы. При этом отличия, конечно же, будут. Мы анализируем лучшие практики, успешный опыт и видим, что зацикливаться только на одной программе нецелесообразно. Должна быть линейка образовательных программ, вмещающая программы разного объема, уровня сложности и учитывающая многие другие особенности. 
