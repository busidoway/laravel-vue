                    <p>
                        <div>
                            <p>- Нет, я рассуждаю иначе. У действующих и потенциальных налоговых консультантов должен быть выбор. Каждый налоговый консультант должен иметь возможность выбрать сообщество, соответствующее его интересам и отвечающее его профессиональным запросам. Одна организация не в состоянии обеспечить профессиональные потребности и оправдать ожидания налоговых консультантов всей страны. Профессиональных членских организаций в сфере налогового консультирования должно быть больше.</p>
                            <p>В отсутствие конкуренции трудно оставаться динамичной структурой. В любой деятельности, и в нашей тоже, без альтернативы и здоровой конкурентной среды развитие невозможно.</p>
